<h2>What is this?</h2>
<p>This is a simple mashup demonstrating the APIs from Soundcloud and Songkick.</p>

<h2>What does this do?</h2>
<p>This app writes the upcoming tour dates from a Songkick artist to your Soundcloud profile.</p>

<h2>What doesn't this do?</h2>
<p> This app does not store your user credentials or account information.</p>

